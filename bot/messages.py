def format_result(text: str, elapsed: float, avg_confidence: float | None) -> str:
    parts = [
        "<b>📄 Распознанный текст:</b>",
        f"<pre>{_escape_html(text)}</pre>",
        f"⏱ Время обработки: <b>{elapsed:.2f} с</b>",
    ]
    if avg_confidence is not None:
        parts.append(
            f"📊 Средняя уверенность модели: <b>{avg_confidence * 100:.1f}%</b> "
            f"({_confidence_label(avg_confidence)})"
        )
    return "\n".join(parts)


def _confidence_label(value: float) -> str:
    if value >= 0.85:
        return "высокая"
    if value >= 0.6:
        return "средняя"
    return "низкая"


def _escape_html(text: str) -> str:
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
    )
